package com.company.books.examples.junit;

public class Calculator {

    Double sum(Double number1, Double number2) {
        return number1 + number2;
    }

    Integer dividir(Integer number1, Integer number2) {
        return number1/number2;
    }

    Integer multiplication(Integer number1, Integer number2) {
        return number1 * number2;
    }

    Integer restar(Integer number1, Integer number2) {
        return number1 - number2;
    }
}
